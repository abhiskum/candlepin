package org.candlepin.functional;

import org.jboss.shrinkwrap.api.ShrinkWrap;
import org.jboss.shrinkwrap.api.spec.WebArchive;
import org.junit.jupiter.api.extension.AfterAllCallback;
import org.junit.jupiter.api.extension.BeforeAllCallback;
import org.junit.jupiter.api.extension.ExtensionContext;

/**
 * Extension that deploys Candlepin via Cargo
 */
public class CargoExtension implements AfterAllCallback, BeforeAllCallback {
    @Override
    public void beforeAll(ExtensionContext context) throws Exception {
        WebArchive war = ShrinkWrap.create(WebArchive.class, "candlepin.war").addPackage("org.candlepin");
    }

    @Override
    public void afterAll(ExtensionContext context) throws Exception {

    }
}
